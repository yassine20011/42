void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	s;

	a = 0;
	b = size - 1;
	while (a < size / 2)
	{
		s = tab[b];
		tab[b] = tab[a];
		tab[a] = s;
		a++;
		b--;
	}
}
